enum OpponentMove {
  Rock = "A",
  Paper = "B",
  Scissors = "C",
}

enum YourMove {
  Rock = "X",
  Paper = "Y",
  Scissors = "Z",
}

enum Outcome {
  Win = "win",
  Draw = "draw",
  Lose = "lose",
}

// Scoring dictionaries
const moveScores: Record<YourMove, number> = {
  [YourMove.Rock]: 1,
  [YourMove.Paper]: 2,
  [YourMove.Scissors]: 3,
};

const outcomeScores: Record<Outcome, number> = {
  [Outcome.Win]: 6,
  [Outcome.Draw]: 3,
  [Outcome.Lose]: 0,
};

const opponentBeats: Record<OpponentMove, YourMove> = {
  [OpponentMove.Rock]: YourMove.Scissors,
  [OpponentMove.Paper]: YourMove.Rock,
  [OpponentMove.Scissors]: YourMove.Paper,
};

const youBeat: Record<YourMove, OpponentMove> = {
  [YourMove.Rock]: OpponentMove.Scissors,
  [YourMove.Paper]: OpponentMove.Rock,
  [YourMove.Scissors]: OpponentMove.Paper,
};

function isOpponentMove(move: string): move is OpponentMove {
  return Object.values(OpponentMove).includes(move as OpponentMove);
}

function isYourMove(move: string): move is YourMove {
  return Object.values(YourMove).includes(move as YourMove);
}

function calculateTotalScore(data: string): number {
  let totalScore = 0;
  const lines = data.split("\n");

  for (const line of lines) {
    const [opponentMoveChar, yourMoveChar] = line.trim().split(" ");

    if (!isOpponentMove(opponentMoveChar) || !isYourMove(yourMoveChar)) {
      console.error(`Invalid move: ${opponentMoveChar} ${yourMoveChar}`);
      continue;
    }

    const opponentMove = opponentMoveChar as OpponentMove;
    const yourMove = yourMoveChar as YourMove;

    const moveScore = moveScores[yourMove];
    let outcomeScore = 0;

    if (youBeat[yourMove] === opponentMove) {
      outcomeScore = outcomeScores[Outcome.Win];
    } else if (opponentBeats[opponentMove] === yourMove) {
      outcomeScore = outcomeScores[Outcome.Lose];
    } else {
      outcomeScore = outcomeScores[Outcome.Draw];
    }

    totalScore += moveScore + outcomeScore;
  }

  return totalScore;
}


// PART 2
enum ExpectedOutcome {
  Lose = "X",
  Draw = "Y",
  Win = "Z",
}

const convertOutcome = {
  [ExpectedOutcome.Win]: Outcome.Win,
  [ExpectedOutcome.Draw]: Outcome.Draw,
  [ExpectedOutcome.Lose]: Outcome.Lose,
};

const outcomeToMove: Record<OpponentMove, Record<ExpectedOutcome, YourMove>> = {
  [OpponentMove.Rock]: {
    [ExpectedOutcome.Lose]: YourMove.Scissors,
    [ExpectedOutcome.Draw]: YourMove.Rock,
    [ExpectedOutcome.Win]: YourMove.Paper,
  },
  [OpponentMove.Paper]: {
    [ExpectedOutcome.Lose]: YourMove.Rock,
    [ExpectedOutcome.Draw]: YourMove.Paper,
    [ExpectedOutcome.Win]: YourMove.Scissors,
  },
  [OpponentMove.Scissors]: {
    [ExpectedOutcome.Lose]: YourMove.Paper,
    [ExpectedOutcome.Draw]: YourMove.Scissors,
    [ExpectedOutcome.Win]: YourMove.Rock,
  },
};

function isExpectedOutcome(outcome: string): outcome is ExpectedOutcome {
  return Object.values(ExpectedOutcome).includes(outcome as ExpectedOutcome);
}

function calculateTotalScoreWithOutcome(data: string): number {
  let totalScore = 0;
  const lines = data.split("\n");

  for (const line of lines) {
    const [opponentMoveChar, yourMoveChar] = line.trim().split(" ");

    if (!isOpponentMove(opponentMoveChar) || !isExpectedOutcome(yourMoveChar)) {
      console.error(`Invalid move or outcome: ${opponentMoveChar} ${yourMoveChar}`);
      continue;
    }

    const opponentMove = opponentMoveChar as OpponentMove;
    const yourMove = outcomeToMove[opponentMove][yourMoveChar as ExpectedOutcome];

    const moveScore = moveScores[yourMove as YourMove]; // Explicitly specify the type of the index
    const outcomeScore = outcomeScores[convertOutcome[yourMoveChar as ExpectedOutcome]];

    totalScore += moveScore + outcomeScore;
  }

  return totalScore;
}
export function combinedScoreOutput(data: string): string {
  const scorePart1 = calculateTotalScore(data);
  const scorePart2 = calculateTotalScoreWithOutcome(data);
  return `Total score for Part 1: ${scorePart1}\nTotal score for Part 2: ${scorePart2}`;
}

